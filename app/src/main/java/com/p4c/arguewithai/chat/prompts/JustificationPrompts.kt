    fun getPromptByIndex(index: Int, prevAnswer: String = ""): String {
        return when (index) {
            0 -> firstPrompt(prevAnswer)
            1 -> secondPrompt(prevAnswer)
            2 -> thirdPrompt(prevAnswer)
            else -> finalPrompt()
        }
    }

